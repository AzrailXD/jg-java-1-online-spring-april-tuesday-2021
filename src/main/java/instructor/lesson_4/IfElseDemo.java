package instructor.lesson_4;

class IfElseDemo {

    public static void main(String[] args) {

        boolean f1 = true;
        boolean f2 = true;

        if (f1) {
            System.out.println("f1"); // 1
        } else {
            System.out.println("f1 - else"); // 2
        }

        if (f2) {
            System.out.println("f2"); // 3
        } else {
            System.out.println("f2 - else"); // 4
        }

    }
}
