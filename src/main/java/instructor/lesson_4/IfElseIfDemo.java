class IfElseIfDemo {

    public static void main(String[] args) {

        boolean f1 = false;
        boolean f2 = false;
        boolean f3 = false;

        if (f1) {
            System.out.println("f1");
        } else if (f2) {
            System.out.println("f2");
        } else if (f3) {
            System.out.println("f3");
        }

    }
}
